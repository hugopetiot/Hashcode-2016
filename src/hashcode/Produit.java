package hashcode;

public class Produit {
	private int id;
	private int weight;
	public Produit(int i,int w){
		id=i;
		weight=w;
	}
	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}
	public int getWeight() {
		return weight;
	}
	public void setWeight(int weight) {
		this.weight = weight;
	}
	
	

}
